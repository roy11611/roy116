// 점수 집계 문제
    //테이스 케이스 t입력
    //각 테스트 케이스 마다 점수 5개를 공백을 두고 입력
    //각 점수는 최저 1점 최고 10점 까지 주어짐
    // 점수 계산 방식은 다음과 같음
    // 1. 기본적으로 5개 점수 중 최저 점수와 최고 점수를 제외한 나머지 3개의 점수의 합이 최종 점수가 됨
    // 2. 만일 최고점과 최저점이 여러개 일 경우 하나만 제외하고 나머지는 포함
    // 3. 만약 최저 점수와 최고 점수를 뺀 나머지 3개의 점수의 최고점수와 최저점수의 차가 4점 이상 알 경우 KIN 출력
    // 4. 만약 케이스의 모든 점수가 동일 할 경우 점수 3개의 합 출력
    #include <stdio.h>

int main() {
    int T;
    scanf("%d", &T); // 테스트 케이스 개수 입력

    while (T--) {
        int score[5];
        
        // 5개의 점수 입력
        for (int i = 0; i < 5; i++) {
            scanf("%d", &score[i]);
        }

        // 1. 버블 정렬 (오름차순 정렬)
        // 5개밖에 안 되므로 단순한 이중 반복문으로 정렬
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 4 - i; j++) {
                if (score[j] > score[j + 1]) {
                    int temp = score[j];
                    score[j] = score[j + 1];
                    score[j + 1] = temp;
                }
            }
        }

        // 정렬 후 배열 상태:
        // score[0]: 최저점 (제거 대상)
        // score[1], score[2], score[3]: 중간 점수들
        // score[4]: 최고점 (제거 대상)

        // 2. 조건 확인 (남은 점수 중 최고점(3번) - 최저점(1번))
        if (score[3] - score[1] >= 4) {
            printf("KIN\n");
        } else {
            // 3. 총점 출력
            printf("%d\n", score[1] + score[2] + score[3]);
        }
    }

    return 0;
}